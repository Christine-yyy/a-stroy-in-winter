int select1(int maxn)
{
//select进行输入合法判断，返回的是输入的值，若非法输入，则返回-1，
//传入的int类型的变量是菜单中的最大选项数
    int select;
    char s;
    scanf("%c",&s);
    //fflush(stdin);//习惯性地清除输入流
    select = (int)(s - 48);//把输入的字符转成int类型的
    if (select < 1 || select > maxn)//如果输入的比1小或者比传入的最大值大，就是非法输入了
    {
        return -1;
    }
    else
        return select;
}
